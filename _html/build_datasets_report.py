def png_b64(name):
    p = os.path.join(PROBE, name)
    return base64.b64encode(open(p, "rb").read()).decode() if os.path.exists(p) else None


def img(name, caption):
    b = png_b64(name)
    if not b:
        return f"<p><em>(missing render: {name})</em></p>"
    return f'<figure><img src="data:image/png;base64,{b}"><figcaption>{caption}</figcaption></figure>'


